package rozdzial04;

public class Cw08Switch {
	public static void main(String[]args){
		for (int i=1; i<5; i++){
			switch (i){
				case 1: System.out.println("Jeden!!");
						break;
				case 2: System.out.println("Dwa!!");
						break;
				case 3: System.out.println("Trzy!!");
						break;
				case 4: System.out.println("Cztery!!");
						break;
			}		
		}
	}
}
